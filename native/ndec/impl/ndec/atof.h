/*
 * atof.h is a self-contained floating-point parser for decimal text.
 *
 * Include this header directly. It defines only static or static inline symbols,
 * so each translation unit gets its own copy and no separate implementation file
 * is required.
 *
 * Public entry points:
 *   atof_parse_f64_ctx                 General decimal; accepts NaN/Inf, +/-, arbitrary leading zeros
 *   atof_parse_f64_padded_ctx          Same as above, but the caller guarantees padding at the buffer tail
 *   atof_parse_f64_json_ctx            Strict JSON number grammar (see below)
 *   atof_parse_f64_json_padded_ctx     Same as above, with padding
 *   atof_parse_f32_ctx
 *   atof_parse_f32_json_ctx
 *
 * Public bit helpers:
 *   atof_bits_f64 / atof_bits_f32      double/float -> uint64/uint32 bitwise reinterpretation
 *
 * The general entries (without the _json_ infix) accept the lenient set of
 * leading zeros, optional sign, and NaN/Inf literals. The JSON entries
 * accept only '-' for the sign and reject '+', NaN, and Inf. The token
 * boundary is set by the caller via [s, s+len); the implementation only
 * scans within that range and does not assume every byte inside is a
 * digit (see the comment block on atof_parse_f64_json_padded_ctx for
 * details).
 *
 * Callers provide reusable storage whose size and alignment are reported by
 * atof_ctx_size() and atof_ctx_align(). The storage is used only by rare trunc
 * paths that need multi-precision correction; common paths do not touch it.
 *
 * The padded JSON entry assumes the buffer tail is safely readable for SWAR
 * loads, while len describes the already-scanned numeric token prefix and may
 * include trailing whitespace. These are independent contracts: padding protects
 * memory access, len preserves token boundaries.
 *
 */
