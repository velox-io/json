package main

import (
	"fmt"
	"html"
	"math"
	"strings"
)

// Layout constants — all coordinates are within a 1100px wide SVG.
const (
	svgWidth   = 1100
	padLeft    = 40
	padRight   = 40
	padTop     = 30
	headerH    = 90  // title + subtitle + legend
	groupGap   = 24  // vertical gap between groups
	footerH    = 40
	barH       = 20  // bar height
	barSpacing = 7   // vertical spacing between bars
	barRx      = 3   // bar corner radius
	sectionHdr = 28  // group section header height
	colHdr     = 16  // column header row height

	// Column layout: libLabel | nsOp chart | gap | bOp chart | gap | allocs chart
	libLabelW = 68
	nsBarW    = 370  // max bar width for ns/op
	bBarW     = 220  // max bar width for B/op
	aBarW     = 130  // max bar width for allocs/op
	colGapW   = 10   // gap between columns
	// Text annotation widths (space after bar for value + badge)
	nsAnnotW = 120
	bAnnotW  = 70
	aAnnotW  = 60
)

// RenderSVG generates the complete SVG string from parsed benchmark data.
func RenderSVG(data *BenchData) string {
	var b strings.Builder

	nLibs := len(data.Libs)
	nGroups := len(data.Groups)
	rowH := barH + barSpacing
	groupBodyH := nLibs*rowH + 4
	groupH := sectionHdr + colHdr + groupBodyH

	totalH := padTop + headerH + nGroups*(groupH+groupGap) + footerH

	// SVG open
	fmt.Fprintf(&b, `<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 %d %d" font-family="%s" font-size="13">`+"\n",
		svgWidth, totalH, FontMono)
	b.WriteString(svgStyles())

	// === Header ===
	title := data.Title
	if title == "" {
		title = "Benchmark Results"
	}
	y := padTop + 24
	fmt.Fprintf(&b, `  <text x="%d" y="%d" text-anchor="middle" class="title">%s</text>`+"\n",
		svgWidth/2, y, esc(title))

	if data.Subtitle != "" {
		y += 20
		fmt.Fprintf(&b, `  <text x="%d" y="%d" text-anchor="middle" class="subtitle">%s</text>`+"\n",
			svgWidth/2, y, esc(data.Subtitle))
	}

	// Legend
	y += 26
	renderLegend(&b, data.Libs, y)

	// === Groups ===
	groupY := padTop + headerH
	for _, group := range data.Groups {
		renderGroup(&b, data, group, groupY, groupH)
		groupY += groupH + groupGap
	}

	// === Footer ===
	footerY := groupY + 10
	countStr := ""
	if data.RunCount > 0 {
		countStr = fmt.Sprintf("  |  count=%d (median)", data.RunCount)
	}
	fmt.Fprintf(&b, `  <text x="%d" y="%d" text-anchor="middle" class="dim" font-size="11">Generated by benchviz%s</text>`+"\n",
		svgWidth/2, footerY, countStr)

	b.WriteString("</svg>\n")
	return b.String()
}

func svgStyles() string {
	return fmt.Sprintf(`  <style>
    .title { font-size: 22px; font-weight: bold; fill: %s; font-family: %s; }
    .subtitle { font-size: 13px; fill: %s; font-family: %s; }
    .grp-title { font-size: 14px; font-weight: bold; fill: %s; font-family: %s; }
    .col-hdr { font-size: 10px; fill: %s; font-weight: 600; font-family: %s; letter-spacing: 0.3px; }
    .lib-label { font-size: 11px; font-weight: bold; }
    .bar-val { font-size: 10.5px; fill: %s; }
    .badge-fast { font-size: 9.5px; fill: %s; font-weight: bold; }
    .badge-ratio { font-size: 9.5px; fill: %s; font-weight: bold; }
    .dim { fill: %s; }
  </style>
`, ColorTitle, FontSans,
		ColorSubtitle, FontSans,
		ColorTitle, FontSans,
		ColorDim, FontSans,
		ColorText,
		ColorFastest,
		ColorSlowest,
		ColorDim)
}

func renderLegend(b *strings.Builder, libs []string, y int) {
	// Center the legend
	totalW := 0
	for _, lib := range libs {
		totalW += 14 + 5 + len(lib)*7 + 24
	}
	x := (svgWidth - totalW) / 2

	for _, lib := range libs {
		c := LibraryColor(lib)
		fmt.Fprintf(b, `  <rect x="%d" y="%d" width="14" height="14" rx="3" fill="%s" opacity="0.85"/>`+"\n",
			x, y-11, c)
		fmt.Fprintf(b, `  <text x="%d" y="%d" class="lib-label" fill="%s">%s</text>`+"\n",
			x+19, y, c, esc(lib))
		x += 14 + 5 + len(lib)*7 + 24
	}
}

func renderGroup(b *strings.Builder, data *BenchData, group string, gy, groupH int) {
	libs := data.Libs
	gr := data.Results[group]
	contentW := svgWidth - padLeft - padRight

	fmt.Fprintf(b, "\n  <!-- %s -->\n", esc(group))

	// Card background
	fmt.Fprintf(b, `  <rect x="%d" y="%d" width="%d" height="%d" rx="8" fill="%s" stroke="%s" stroke-width="1"/>`+"\n",
		padLeft, gy, contentW, groupH, ColorCardBg, ColorCardBdr)

	// Group title (left-aligned)
	displayName := strings.ReplaceAll(group, "_", " ")
	fmt.Fprintf(b, `  <text x="%d" y="%d" class="grp-title">%s</text>`+"\n",
		padLeft+14, gy+20, esc(displayName))

	// Column start x positions
	// Layout: | padLeft+14 | libLabelW | nsBar+nsAnnot | gap | bBar+bAnnot | gap | aBar+aAnnot |
	x0 := padLeft + 14 // library labels
	nsX := x0 + libLabelW
	bX := nsX + nsBarW + nsAnnotW + colGapW
	aX := bX + bBarW + bAnnotW + colGapW

	// Column headers
	hdrY := gy + sectionHdr + 10
	fmt.Fprintf(b, `  <text x="%d" y="%d" class="col-hdr">ns/op (lower is better)</text>`+"\n", nsX, hdrY)
	fmt.Fprintf(b, `  <text x="%d" y="%d" class="col-hdr">B/op</text>`+"\n", bX, hdrY)
	fmt.Fprintf(b, `  <text x="%d" y="%d" class="col-hdr">allocs/op</text>`+"\n", aX, hdrY)

	// Compute min/max per metric
	minNs, maxNs := math.MaxFloat64, 0.0
	minB, maxB := math.MaxFloat64, 0.0
	minA, maxA := math.MaxFloat64, 0.0
	for _, lib := range libs {
		r, ok := gr[lib]
		if !ok {
			continue
		}
		minNs = math.Min(minNs, r.NsOp)
		maxNs = math.Max(maxNs, r.NsOp)
		minB = math.Min(minB, r.BOp)
		maxB = math.Max(maxB, r.BOp)
		minA = math.Min(minA, r.AllocsOp)
		maxA = math.Max(maxA, r.AllocsOp)
	}

	barsTopY := hdrY + 8
	for li, lib := range libs {
		r, ok := gr[lib]
		if !ok {
			continue
		}
		rowY := barsTopY + li*(barH+barSpacing)
		color := LibraryColor(lib)
		textY := rowY + barH/2 + 4

		// Library label
		fmt.Fprintf(b, `  <text x="%d" y="%d" class="lib-label" fill="%s">%s</text>`+"\n",
			x0, textY, color, esc(lib))

		// ns/op bar + annotation
		renderMetricBar(b, nsX, rowY, nsBarW, r.NsOp, maxNs, minNs, color, FormatNsOp(r.NsOp), true)

		// B/op bar + annotation
		renderMetricBar(b, bX, rowY, bBarW, r.BOp, maxB, minB, color, FormatBytes(r.BOp), false)

		// allocs/op bar + annotation
		renderMetricBar(b, aX, rowY, aBarW, r.AllocsOp, maxA, minA, color, FormatAllocs(r.AllocsOp), false)
	}
}

// renderMetricBar draws a single horizontal bar with value annotation.
// If showRatio is true, adds "x.xX" or "fastest" badge.
func renderMetricBar(b *strings.Builder, x, y, maxW int, val, maxVal, minVal float64, color, label string, showRatio bool) {
	// Bar width proportional to max
	bw := 4
	if maxVal > 0 {
		bw = int(float64(maxW) * val / maxVal)
		if bw < 4 {
			bw = 4
		}
	}

	// Draw bar
	fmt.Fprintf(b, `  <rect x="%d" y="%d" width="%d" height="%d" rx="%d" fill="%s" opacity="0.80"/>`+"\n",
		x, y, bw, barH, barRx, color)

	textY := y + barH/2 + 4
	labelX := x + bw + 5

	if showRatio && minVal > 0 {
		isFastest := val <= minVal*1.03 // within 3% = essentially tied
		if isFastest {
			fmt.Fprintf(b, `  <text x="%d" y="%d" class="bar-val">%s</text>`+"\n", labelX, textY, esc(label))
			fmt.Fprintf(b, `  <text x="%d" y="%d" class="badge-fast"> ★ fastest</text>`+"\n",
				labelX+len(label)*7, textY)
		} else {
			ratio := val / minVal
			fmt.Fprintf(b, `  <text x="%d" y="%d" class="bar-val">%s </text>`+"\n", labelX, textY, esc(label))
			fmt.Fprintf(b, `  <text x="%d" y="%d" class="badge-ratio">%.1fx</text>`+"\n",
				labelX+len(label)*7+3, textY, ratio)
		}
		return
	}

	// Non-ratio columns: just value, with optional ★ for lowest
	isLowest := minVal >= 0 && val <= minVal*1.005 && maxVal > minVal*1.01
	fmt.Fprintf(b, `  <text x="%d" y="%d" class="bar-val">%s</text>`+"\n", labelX, textY, esc(label))
	if isLowest {
		fmt.Fprintf(b, `  <text x="%d" y="%d" class="badge-fast"> ★</text>`+"\n",
			labelX+len(label)*7, textY)
	}
}

func esc(s string) string {
	return html.EscapeString(s)
}
